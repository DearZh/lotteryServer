package admin.domains.content.biz.utils;

import java.util.List;
import java.util.ArrayList;

public class JSTreeVO
{
    private String id;
    private String text;
    private String icon;
    private ArrayList<JSTreeVO> children;
    
    public JSTreeVO() {
        this.children = new ArrayList<JSTreeVO>();
    }
    
    public String getId() {
        return this.id;
    }
    
    public void setId(final String id) {
        this.id = id;
    }
    
    public String getText() {
        return this.text;
    }
    
    public void setText(final String text) {
        this.text = text;
    }
    
    public String getIcon() {
        return this.icon;
    }
    
    public void setIcon(final String icon) {
        this.icon = icon;
    }
    
    public List<JSTreeVO> getChildren() {
        return this.children;
    }
    
    public void setChildren(final ArrayList<JSTreeVO> children) {
        this.children = children;
    }
}
